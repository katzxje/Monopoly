/**
 * Defines the types of spaces on the Monopoly board.
 */
public enum SpaceType {
    GO,                 // Starting space
    PROPERTY,           // Regular property space
    RAILROAD,           // Railroad space
    UTILITY,            // Utility space
    TAX,                // Tax space
    CHANCE,             // Chance card space
    COMMUNITY_CHEST,    // Community Chest card space
    JAIL,               // Jail space (visiting or incarcerated)
    GO_TO_JAIL,         // Go To Jail space
    FREE_PARKING        // Free Parking space
}